DATA SEGMENT PARA PUBLIC 'DATA'
NUMBERS DB 10 DUP(?)
DATA ENDS
CODE SEGMENT PARA PUBLIC 'CODE'

CONVERT PROC NEAR
MOV BL, 10
DIV BL
XCHG AH, AL
ADD AH, '0';make them start right in ascii
ADD AL, '0'

RET
CONVERT ENDP

START PROC FAR
ASSUME CS:CODE, DS:DATA

PUSH DS ;prepare to return to DOS
XOR AX,AX
PUSH AX
MOV AX,DATA; initialise DS
MOV DS, AX

XOR AH, AH
MOV AL, 0FH;take one 
CALL CONVERT
RET
START ENDP

CODE ENDS

END START