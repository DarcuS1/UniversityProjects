
DATA SEGMENT PARA PUBLIC 'DATA'

DATA ENDS

CODE SEGMENT PARA PUBLIC 'CODE'
ASSUME CS:CODE,DS:DATA


START PROC FAR
PUSH DS
MOV AX,DATA
MOV DS,AX

MOV AX,5
;RANDOM VALUE TO AX
MOV CL,7
;GIVE 7 TO CL FOR LATER MULTIPLICATION
MUL CL ;AL*CL
;or use shift
;MOV DX,AX ;STORE INITIAL 
;SHL AX,3  ;AX*8
;SUB DX,AX   ;AX*8-INITIAL AX
;MOV AX,DX   ;GIVE AX THE VALUE OF AX*7
MOV BX,5
;RANDOM VALUE TO BX

SHL BX,1 ;BX*2
SUB AX,BX;SUBSTRACT BX FROM AX
SHR BX,4 ;BX/8, 4 because bx was multiplied by 2
SUB AX,BX

POP DS
RET

START ENDP
CODE ENDS
END START
