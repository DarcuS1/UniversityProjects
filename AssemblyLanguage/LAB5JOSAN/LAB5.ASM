
DATA SEGMENT PARA PUBLIC 'DATA'
NR1 DB 15
;WE GIVE A RANDOM VALUE TO NR1
NR2 DB 10
;WE GIVE A RANDOM VALUE TO NR2
REZ DW 0
;THE RESULT IS WORD, INITIALIZED WITH 0
DATA ENDS

CODE SEGMENT PARA PUBLIC 'CODE'
ASSUME CS:CODE,DS:DATA


START PROC FAR
PUSH DS
MOV AX,DATA
MOV DS,AX

MOV AX,5
;RANDOM VALUE TO AX
MOV BL,NR1
;WE MOVE NR1 IN BX FOR LATER MULTIPLICATION
MOV DL, NR2
;SAME WITH NR2
MOV CX,0
;CLEAN CX

MOV  SI,AX
;STORE AX FOR LAETR USE

MUL BL ;AL*NR1
;WE COULD HAVE USED IMUL FOR SIGNED 
ADD  CX,AX
;ADD RESULT OF FIRST EQUATION TO CX

MOV AX,SI
;REGIVE VALUE TO AX

MUL DL ; AL*NR2
ADD CX,AX
;ADD RESULT OF SECOND OPERATION TO CX

ADD CX,BX
;ADD FINAL


MOV REZ,CX
;MOVE SUM RESULT TO REZ



POP DS
RET
START ENDP
CODE ENDS
END START
