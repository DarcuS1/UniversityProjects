DATA SEGMENT PARA PUBLIC 'DATA'

STR1 DB 5 DUP(?)
;string liber cu 5 locuri
DATA ENDS

CODE SEGMENT PARA PUBLIC 'CODE'
ASSUME CS:CODE,DS:DATA


START PROC FAR
PUSH DS
MOV AX,DATA
MOV DS,AX

MOV CX,5 ;pregatirea loopului
MOV SI,0  ;initializare
XOR AX,AX ;CURATAM AX
MOV BX,65
MOV AX,BX      ;PUNEM O VALOARE RANDOM PE AX

next :
MOV STR1[SI],AL   ;MUTAM IN STRING
INC SI          ;INCREMENT LA SI SI AL PT URMATOAREA VLOARE
INC AL 

loop next

POP DS
RET

START ENDP
CODE ENDS
END START
