
DATA1 SEGMENT PARA PUBLIC 'DATA'
STR1 DB "LOREMIPSUM"
STR1LEN EQU $-STR1
DATA1 ENDS


DATA2 SEGMENT PARA PUBLIC 'DATA'

STR2 DB ?


DATA2 ENDS

CODE SEGMENT PARA PUBLIC 'CODE'
ASSUME CS:CODE,DS:DATA1


START PROC FAR
PUSH DS
MOV AX,DATA1
MOV DS,AX

XOR CX,CX
MOV CX,STR1LEN

MOV SI,0

mover:

MOV AL,STR1[SI]
INC SI
PUSH AX
loop mover


ASSUME DS:DATA2
MOV AX,DATA2
MOV DS,AX

taker:

POP AX
MOV STR2[SI],AL
DEC SI

loop taker



POP DS
RET

START ENDP
CODE ENDS
END START
